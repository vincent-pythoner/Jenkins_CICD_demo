# some_module.py
def add(a, b):              # 1. 函数定义
    return a + b            # 2. 返回值

def subtract(a, b):         # 3. 函数定义
    return a - b            # 4. 返回值

def multiply(a, b):         # 5. 函数定义
    return a * b            # 6. 返回值

def divide(a, b):           # 7. 函数定义
    if b == 0:              # 8. 条件判断
        return None         # 9. 返回值
    return a / b            # 10. 返回值

